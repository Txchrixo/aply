const DICTS: Record<Locale, Dict> = { en, fr, de };

interface I18nContextValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string) => string;
}

const I18nContext = createContext<I18nContextValue>({
  locale: "en",
  setLocale: () => {},
  t: (k: string) => k,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  const setLocale = useCallback((l: Locale) => {
    setLocaleState(l);
    try {
      localStorage.setItem("aply-locale", l);
    } catch {
      /* ignore */
    }
  }, []);

  // Hydrate from localStorage on mount
  useState(() => {
    try {
      const saved = localStorage.getItem("aply-locale") as Locale | null;
      if (saved && ["en", "fr", "de"].includes(saved)) {
        setLocaleState(saved);
      }
    } catch {
      /* ignore */
    }
  });

  const t = useCallback(
    (key: string) => DICTS[locale][key] ?? DICTS.en[key] ?? key,
    [locale]
  );

  return (
    <I18nContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
